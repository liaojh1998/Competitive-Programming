vector<int> G[100005], cand;
bool vis[100005], possible = false;
void firstdfs(int u){
	if(vis[u]){
		cand.push_back(u);
		return;
	}
	vis[u] = true;
	int size = G[u].size();
	for(int i = 0; i < size; i++)
		firstdfs(G[u][i]);
}
void seconddfs(int u, int candi){
	if(vis[u] && u == candi){
		possible = true;
		return;
	}
	if(vis[u])
		return;
	vis[u] = true;
	int size = G[u].size();
	for(int i = 0; i < size && !possible; i++)
		seconddfs(G[u][i], candi);
}
void solve(){
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int N, M, u, v;
	readInt(N);
	readInt(M);
	for(int i = 0; i < M; i++){
		readInt(u);
		readInt(v);
		G[u].push_back(v);
	}
	memset(vis, 0, sizeof(vis));
	for(int i = 1; i <= N; i++){
		if(!vis[i]) firstdfs(i);
	}
	int size = cand.size();
	for(int i = 0; i < size && !possible; i++){
		memset(vis, 0, sizeof(vis));
		seconddfs(cand[i], cand[i]);
	}
	printf("%s\n", possible ? "YES" : "NO");
}
int main(){
	solve();
	return 0;
}